package db.model;

import java.security.MessageDigest;
import java.security.NoSuchAlgorithmException;

import thrift.ErrorType;

import com.googlecode.objectify.ObjectifyService;
import com.googlecode.objectify.annotation.Entity;
import com.googlecode.objectify.annotation.Id;
import com.googlecode.objectify.annotation.Index;

import error.HAPIException;

@Entity
public class User {

    // register the object model
    static {
        ObjectifyService.register(User.class);
    }

    @Id
    @Index
    private String email;
    // hashed password
    private String password;
    private String name;
    private String nickName;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) throws HAPIException {
        try {
            MessageDigest m = MessageDigest.getInstance("MD5");
            this.password = new String(m.digest(password.getBytes()));
        } catch (NoSuchAlgorithmException e) {
            throw new HAPIException(ErrorType.UNKNOWN_ERROR);
        }
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((email == null) ? 0 : email.hashCode());
        result = prime * result
                + ((password == null) ? 0 : password.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        User other = (User) obj;
        if (email == null) {
            if (other.email != null)
                return false;
        } else if (!email.equals(other.email))
            return false;
        if (password == null) {
            if (other.password != null)
                return false;
        } else if (!password.equals(other.password))
            return false;
        return true;
    }

}
